    private void setTableData(List<Request> requests) {
        defaultTableModel.setRowCount(0); // Xóa dữ liệu cũ

        for (Request r : requests) {
            defaultTableModel.addRow(new Object[]{
                r.getRequestId(),
                r.getCustomerId(),
                r.getEmployeeId() == null ? "No employee Avaible" : r.getEmployeeId(), // Xử lý nếu nhân viên NULL
                r.getRequestType(),
                r.getDescription(),
                r.getStatus(),
                r.getDateCreated(),
                r.getDateResolved() == null ? "Waiting" : r.getDateResolved()
            });
        }
    }

